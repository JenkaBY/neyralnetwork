package by.jenka.ann.network;

public class HyperParams {
    private float speed;
    private float moment;

    public HyperParams(float speed, float moment) {
        this.speed = speed;
        this.moment = moment;
    }

    public float getSpeed() {
        return speed;
    }

    public float getMoment() {
        return moment;
    }
}
